    private void ShowAvailRooms()
    {
        try {
            Class.forName("java.sql.Driver");
            conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/hms","root","");
            stmt = conn.createStatement();
            String sql = "select * from room where Booking = 'Unbooked'";
            rs = stmt.executeQuery(sql);
            while(rs.next())
            {
                RoomNo.addItem(rs.getString(1));
            }
           
        }
        catch (Exception e) 
        {
            JOptionPane.showMessageDialog(null,"Sorry No Rooms Available");
        }
    }
          

