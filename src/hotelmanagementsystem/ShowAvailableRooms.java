    private void BookingActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BookingActionPerformed
        // TODO add your handling code here:         
        int RNO = Integer.parseInt(RoomNo.getSelectedItem().toString());
        try{
            Class.forName("java.sql.Driver");
            Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/hms","root","");
            Statement stmt = conn.createStatement();
            String sql = "SELECT * FROM room WHERE Room_No = "+RNO; 
            ResultSet rs = stmt.executeQuery(sql);
            if(rs.next())
            {
               new BookingRoom(RNO).setVisible(true);
               this.setVisible(false);
           
            }
            else
            {
               JOptionPane.showMessageDialog(null,"Unable to fine Booking number");
            }
        } 
        catch (Exception e) 
        {
            JOptionPane.showMessageDialog(null,e);
            
        }         
    }//GEN-LAST:event_BookingActionPerformed

